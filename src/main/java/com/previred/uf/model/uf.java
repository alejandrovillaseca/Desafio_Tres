package com.previred.uf.model;

public class uf {
    private String fecha;
    private  String dato;

    public uf() {
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public void setDato(String dato) {
        this.dato = dato;
    }

    public String getFecha() {
        return fecha;
    }

    public String getDato() {
        return dato;
    }
}
